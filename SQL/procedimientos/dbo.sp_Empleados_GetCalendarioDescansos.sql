CREATE OR ALTER PROCEDURE [dbo].[sp_Empleados_GetCalendarioDescansos]
    @EmpleadoId  INT,
    @FechaInicio DATE,
    @FechaFin    DATE
AS
BEGIN
    SET NOCOUNT ON;

    SELECT Fecha, TipoDia, OrigenHorario
    FROM dbo.fn_Empleados_GetCalendarioDias(@EmpleadoId, @FechaInicio, @FechaFin)
    WHERE TipoDia <> 'LABORAL'
    ORDER BY Fecha;
END
